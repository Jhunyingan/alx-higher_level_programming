#!/usr/bin/python3

def add(x, y):
    """Returns the sum of x and y."""
    return x + y

def sub(x, y):
    """Returns the difference of x and y."""
    return x - y

def mul(x, y):
    """Returns the product of x and y."""
    return x * y

def div(x, y):
    """Returns the quotient of x and y."""
    return int(x / y)

